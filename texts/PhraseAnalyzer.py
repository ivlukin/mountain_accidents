from nltk import ngrams

from Config.Config import EXCLUDED_PHRASES
from texts.MessageTokenizer import normalize_word

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–∏—Å–∫–∞ –≤—Ö–æ–¥–Ω—ã—Ö —Å–ª–æ–≤–æ—Å–æ—á–µ—Ç–∞–Ω–∏–π –≤ —Ç–µ–∫—Å—Ç–µ
def find_input_phrases(filtered_words, input_phrases):
    # –ù–æ—Ä–º–∞–ª–∏–∑–∞—Ü–∏—è –≤—Ö–æ–¥–Ω—ã—Ö —Å–ª–æ–≤–æ—Å–æ—á–µ—Ç–∞–Ω–∏–π
    normalized_input_phrases = []
    for phrase in input_phrases:
        # –†–∞–∑–¥–µ–ª—è–µ–º —Å–ª–æ–≤–æ—Å–æ—á–µ—Ç–∞–Ω–∏–µ –Ω–∞ —Å–ª–æ–≤–∞ –∏ –Ω–æ—Ä–º–∞–ª–∏–∑—É–µ–º –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ
        normalized_phrase = [normalize_word(word) for word in phrase.split()]
        normalized_input_phrases.append(" ".join(normalized_phrase))

    normalized_excluded_phrases = []
    for phrase in EXCLUDED_PHRASES:
        normalized_phrase = [normalize_word(word) for word in phrase.split()]
        normalized_excluded_phrases.append(" ".join(normalized_phrase))

    # –ì–µ–Ω–µ—Ä–∞—Ü–∏—è n-–≥—Ä–∞–º–º (–±–∏–≥—Ä–∞–º–º, —Ç—Ä–∏–≥—Ä–∞–º–º –∏ —Ç.–¥.) –∏–∑ —Ç–µ–∫—Å—Ç–∞
    found_phrases = []
    for n in range(2, 4):  # –ò—â–µ–º –±–∏–≥—Ä–∞–º–º—ã –∏ —Ç—Ä–∏–≥—Ä–∞–º–º—ã
        n_grams = ngrams(filtered_words, n)
        for gram in n_grams:
            # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º n-–≥—Ä–∞–º–º—É –≤ —Å—Ç—Ä–æ–∫—É
            gram_str = " ".join(gram)
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º –µ—Å—Ç—å –ª–∏ —ç—Ç–∞ n-–≥—Ä–∞–º–º–∞ –≤ —Å–ø–∏—Å–∫–µ –∏—Å–∫–ª—é—á–∞–µ–º—ã—Ö —Å–ª–æ–≤–æ—Å–æ—á–µ—Ç–∞–Ω–∏–π
            if gram_str in normalized_excluded_phrases:
                return None
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —ç—Ç–∞ n-–≥—Ä–∞–º–º–∞ –≤ —Å–ø–∏—Å–∫–µ –≤—Ö–æ–¥–Ω—ã—Ö —Å–ª–æ–≤–æ—Å–æ—á–µ—Ç–∞–Ω–∏–π
            if gram_str in normalized_input_phrases:
                found_phrases.append(gram_str)

    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–ø–∏—Å–æ–∫ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö —Å–ª–æ–≤–æ—Å–æ—á–µ—Ç–∞–Ω–∏–π
    return len(found_phrases)

# –ü—Ä–∏–º–µ—Ä —Ç–µ–∫—Å—Ç–∞ –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏
